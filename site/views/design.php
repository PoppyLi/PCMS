        	<div>
	<div>
		<strong> 设计理念的诠释: 自然和谐、人文关怀、安全低耗</strong> 
	</div>
	<div>
		“自然一词有两个主要的含义：一是指的自然物的集合，二是指“本性”。而我们在此所提倡的健康家居设
	</div>
	<div>
	</div>
	<div>
		<strong> 和谐</strong> 
	</div>
	<div>
		中国古人关于“和谐”的概念中，首先就是把“和”与“利”联系在一起。古人说，利为义之和。也就是说，万物之间当然有利益冲突，只有消弭冲突，各自节制，不相争夺，才能达到和谐， 因此，“和谐”之“和”首先是调和、和解不同利益之“和”。
	</div>
	<div>
	</div>
	<div>
		<strong> 自然和谐</strong> 
	</div>
	<div>
		通过自然物特有的属性遵循自然界的规律和法则，调和室内人文与物件，精神与物体，空间与结构，色调与采光，等多方面的冲突已达到心灵感受上的和谐舒适的健康居室环境。
	</div>
	<div>
	</div>
	<div>
		<strong> 人文</strong> 
	</div>
	<div>
		人类的文化轨迹，人类现状文化背景，和通过改创未来人类文化的发展方向。它包括(历史的，民族的，区域的，个体的，集体的，宗教的，信仰的等)。而我们在这里只研究人类在室内环境的人文学科。
	</div>
	<div>
	</div>
	<div>
		<strong> 关怀</strong> 
	</div>
	<div>
		关怀本是动词，但我们也经常用于形容词的范畴，如关怀式家装，它表示的是一种行为上的状态和程度，关怀代表着，服务，爱心，温馨、恬静、优雅、和谐、奋发向上。
	</div>
	<div>
	</div>
	<div>
		<strong> 人文关怀</strong> 
	</div>
	<div>
		一个蕴含人文关怀的家居环境，会给人一个温馨、恬静、优雅、和谐、奋发向上的精神空间，从这个意义上来说，人文关怀与其说是民族文化的积淀，不如说是时代对室内设计的召唤。室内环境设计呼唤人文关怀！以人为本，关爱弱势人群，构筑无障碍生活。人文关怀设计倡导的是一种既与传统文脉相承、又结合现代功能与技术的要求与之相并进 。同时也记载了居住者在精神健康的文明程度的高低。
	</div>
	<div>
	</div>
	<div>
		<strong> 安全</strong> 
	</div>
	<div>
		从英文SAFETY安全的解释是这样：保险，安全设备，保险装备。安全性在人居生活中体现的是一种生活状态的保障，那么健康家居设计将通过安全设备，安全用料，安全施工，安全的设计手法，便于维护等多方面使得人在任何时间任何状态都处于相对的健康状态。
	</div>
	<div>
	</div>
	<div>
		<strong> 低耗</strong> 
	</div>
	<div>
		所谓健康家居的低耗，即是我们通过设计手段追求消耗最小的能源，资源和环境付出，而获取较大的室内环境质量和服务。低耗同时也是住者一种能源的储备。材料的再生和回收性。
	</div>
	<div>
	</div>
	<div>
		<strong> 安全低耗</strong> 
	</div>
	<div>
		通过安全性的设计手段，在长期的居住环境中避免出现安全隐患问题，并达到最小的消耗获取最大的能源储备。体现出健康家居生活保障。我们有义务在更多的设计中找到低耗的每一种可能，不放过每一种低耗材料的替代品。
	</div>

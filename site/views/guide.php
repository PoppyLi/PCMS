            <h1 class="title">办公设计要在满足功能的前提下控制预算</h1>
            <p class="clicks_addtime">点击次数：<span>102</span>&nbsp;&nbsp;更新时间：2015-10-16</p>
            <div class="editor">
              <div>
	在做设计的时候，首先需要考虑的问题就是，办公室的空间设计，如何来划分办公室的空间，应当使空间的划分看起来比较的合理，现在许多的办公室设计的空间划分很不合理，结果给以后的办公带来了许多的麻烦，这些都是前期设计的时候需要注意的。
</div>
<div>
</div>
<div>
	第一、装修材料选择环保健康的稍微便宜点的材料。 装修材料有很多，品牌也有很多，这样就会让人难以选择。其实则不然优先选择环保健康的材料。在环保健康的基础上考虑价位因素。选择装修材料的时候切记货比三家。除非是你特别信任那个装饰公司你全权委托，即使您全权委托给了装饰公司，也要仔细的看材料采购清单。
</div>
<div>
</div>
<div>
	第二、办公用品的选择考虑国产。 在进行办公室装修设计的时候，就需要考虑办公家具的问题了。如果不是特别需求奢华或者必要的情况下，建议是选择国产的办公家具。同样的款式价格就比国外进口的便宜好几倍，然而质量也是有保障的。
</div>
<div>
</div>
<div>
	第三、选择适合自己要求的设计师。 装饰公司的设计师很多，设计师擅长的风格和领域也不同。但是一个好的设计师能满足甲方办公室装修设计的需求还能有效的控制办公室装修的预算。他可以在部分项目上使用一些物美价廉的材料，还不会因为材料的问题引发返工的纠纷进而耽误工程进度。
</div>
<div>
</div>
<div>
	总之，我们建议在办公室装修设计的时候应当根据办公人员的身份来做不同的设计，尤其是领导者的办公空间一定不可以大意。
</div>              <div class="clear"></div>
